<h3>Searching</h3>
<p>
There is 2 method available on searching library catalog. The first one is <strong>SIMPLE SEARCH</strong>,
which is the simplest method on searching catalog, you just enter any keyword, either it contained
in document titles, authors name or subjects. You can supply more than one keywords in Simple Search method
and it will expanding your search results.
</p>
<br />
<p>
<strong>ADVANCED SEARCH</strong>, lets you define keywords in more specific fields. If you want
your keywords only contained in title field, then type your keyword in Title field and the system will scope
it search only on <strong>Title</strong> field, not in other fields. Location field lets you narrowing search results by specific
location, so only collection that exists in selected location get fetched by system.
</p>